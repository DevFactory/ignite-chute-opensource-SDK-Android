package com.android.getchute.sdk.chutesdkandroid.model;

import com.google.gson.annotations.SerializedName;

public class LoginRequestModel {

    @SerializedName("grant_type")
    String grantType = "password";
    @SerializedName("username")
    String username;
    @SerializedName("password")
    String password;
    @SerializedName("client_id")
    String clientId;
    @SerializedName("client_secret")
    String clientSecret;

    public LoginRequestModel(String username, String password, String clientId, String clientSecret) {
        this.username = username;
        this.password = password;
        this.clientId = clientId;
        this.clientSecret = clientSecret;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }

    public void setClientSecret(String clientSecret) {
        this.clientSecret = clientSecret;
    }

    public String getClientId() {
        return clientId;
    }

    public String getClientSecret() {
        return clientSecret;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    @Override
    public String toString() {
        final StringBuilder sb = new StringBuilder("LoginRequestModel{");
        sb.append("grantType='").append(grantType).append('\'');
        sb.append(", username='").append(username).append('\'');
        sb.append(", password='").append(password).append('\'');
        sb.append(", clientId='").append(clientId).append('\'');
        sb.append(", clientSecret='").append(clientSecret).append('\'');
        sb.append('}');
        return sb.toString();
    }
}
